import mongoose from 'mongoose';

const TechnologySchema = new mongoose.Schema({
  _id: {
    type: Number,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
});

export default mongoose.models.Technology ||
  mongoose.model('Technology', TechnologySchema);
